x = 6
y = 2
z = 7

print(f"{x} mas {y} es igual {x+y}")
print(f"{x} menos  {y} es igual {x-y}")
print(f"{x} por  {y} es igual {x*y}")
print(f"{x} dividido {y} es igual {x/y}")

#division al piso de coloca //
print(f"{z} dividido al piso de  {y} es igual a  {z//y}")

#division con mudulo se coloca %
print(f"{z} modulo de  {y} es igual {z%y}")

#calcular elevado a 2 e coloca **
print(f"{x} elevado a la   {y} es igual a  {x**y}")

#calcular elevado a 3 e coloca **
print(f"{x} elevado a la   {3} es igual a  {x**3}")

#calcular raiz cuadrada  se coloca **0.5
print(f"la raiz cuadra de {x} es igual a  {x**0.5}")

