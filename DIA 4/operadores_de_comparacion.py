#comparacion de son iguales ==
mi_bool = 10==25
print(mi_bool)

mi_bool = 'blanco' == 'Blanco'
print(mi_bool)

mi_bool = 'blanco' == 'Blanco'.lower()
print(mi_bool)

mi_bool = 100 == '100'
print(mi_bool)

mi_bool = 100.00 == 100
print(mi_bool)

#diferente
mi_bool = 100 != 99
print(mi_bool)

#mayor o menor o igual
mi_bool = 5 >= 6
print(mi_bool)

